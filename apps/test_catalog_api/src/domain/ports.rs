//! Порты — интерфейсы, через которые ядро приложения общается с внешним
//! миром. Инфраструктурный слой предоставляет их реализации (адаптеры).
//! Домен и сервисы работают только с этими трейтами и ничего не знают
//! про то, что сейчас всё лежит в памяти, а не в БД.

use async_trait::async_trait;

use super::models::{FileRecord, RateLimitDecision};

/// Каталог "прогресса" кандидата: какие файлы ему назначены и какие из
/// них он уже отметил скачанными. При первом обращении кандидата
/// каталог для него генерируется лениво.
#[async_trait]
pub trait CandidateCatalogPort: Send + Sync {
    /// Вернуть имена файлов, назначенных кандидату, но ещё не
    /// отмеченных им как скачанные. Если кандидат обращается впервые —
    /// каталог для него будет сгенерирован.
    async fn pending_names(&self, candidate_id: &str) -> Vec<String>;

    /// Отметить файлы скачанными для кандидата. Возвращает количество
    /// вновь отмеченных и количество уже отмеченных ранее.
    /// Кандидат создаётся лениво, если обращается впервые.
    async fn mark_downloaded(&self, candidate_id: &str, names: &[String]) -> (u32, u32);

    /// Сбросить прогресс кандидата (удалить отметки о скачанном).
    /// Возвращает true, если данные о кандидате существовали.
    async fn reset(&self, candidate_id: &str) -> bool;
}

/// Глобальное хранилище содержимого файлов: имя -> байты. Файлы в него
/// попадают в момент генерации каталога кандидата (см. `CandidateCatalogPort`).
#[async_trait]
pub trait FileStorePort: Send + Sync {
    /// Проверить, что все переданные имена существуют. Если каких-то
    /// не хватает — вернуть их список.
    async fn check_exist(&self, names: &[String]) -> Result<(), Vec<String>>;

    /// Получить содержимое файлов по именам. Если каких-то не хватает —
    /// вернуть их список как ошибку.
    async fn get_many(&self, names: &[String]) -> Result<Vec<FileRecord>, Vec<String>>;
}

/// Ограничитель частоты запросов, работает по ключу клиента (IP).
#[async_trait]
pub trait RateLimiterPort: Send + Sync {
    /// Проверить и учесть очередной запрос клиента.
    async fn check(&self, client_key: &str) -> RateLimitDecision;

    /// Снять бан и обнулить счётчики нарушений клиента.
    /// Возвращает true, если для клиента были какие-либо данные.
    async fn reset(&self, client_key: &str) -> bool;
}
